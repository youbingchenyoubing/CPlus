#include <iostream>
#include <string> 
using namespace std;  
int main(){
	
	string name="张三"; // string 是数据类型， name是变量名 
	char sex='b'; // char是数据类型，sex是变量名 
	int age =12; // age 是数据类型，age是变量名 
	float english_score=85.5;  // float是数据类型，english_score是变量名 
	cout<<"姓名:"<<name<<" 性别:"<<sex<<" 年龄: "<<age<<" 英语成绩:"<<english_score<<endl;
	
	return 0;                  
}
